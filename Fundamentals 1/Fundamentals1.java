public class Fundamentals1
{
    public static int add(int a, int b) {
        int sum = a + b;
        return sum;
    }
    
    public static int sub(int a, int b) {
        int difference = a - b;
        return difference;
    }
    
    public static int mul(int a, int b) {
        int product = a * b;
        return product;
    }
    
    public static int div(int a, int b) {
        int quotient = a / b;
        return quotient;
    }
    
    public static double addd(double a, double b) {
        double sum = a + b;
        return sum;
    }
    
    public static double subd(double a, double b) {
        double difference = a - b;
        return difference;
    }
    
    public static double muld(double a, double b) {
        double product = a * b;
        return product;
    }
    
    public static double divd(double a, double b) {
        double product = a / b;
        return product;
    }
        
    public static void main ( String args[] )
        {
         System.out.println("Number of students in 7th grade: " + add(19, 24));
        } // end method main
} // end class Fundamentals1